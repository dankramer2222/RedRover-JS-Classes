// comparison operator js

//equal

let isEqual = 5 == "5"
console.log(isEqual);

//strict equal

let isStrictEqual = 5 === '5'
console.log(isStrictEqual);

//not equal
let isNotEqual = 5 != 8
console.log(isNotEqual);

// strict not equal
let strictNotEqual = 5 !== '5'
console.log(strictNotEqual);

//greater then
let greaterN = 5 > 8 
console.log(greaterN);

let lessT = 5 < 8
console.log(lessT);

//greater or equal
let greatOrEqual = 5 >=5
console.log(greatOrEqual);
//less or equal
let lessOrEqual = 5<= 4
console.log(lessOrEqual);